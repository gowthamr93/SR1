using System.Threading.Tasks;

namespace Idox.LGDP.Apps.Common.OnSite
{
	/// ----------------------------------------------------------------------------------------------------
	/// Name			IContentController
	/// 
	/// <summary>		Generic interface definition for managing content loaded and saved to a device.
	/// </summary>
	/// <remarks>
	/// </remarks>
	/// ----------------------------------------------------------------------------------------------------
	/// 
	public interface IContentController
	{
		/// ------------------------------------------------------------------------------------------------
		#region Public Functions, Properties and Methods
		/// ------------------------------------------------------------------------------------------------
		/// 
		/// ------------------------------------------------------------------------------------------------
		/// Name		Clear
		/// 
		/// <summary>	Clears all data on the device.
		/// </summary>
		/// <remarks>
		/// </remarks>
		/// ------------------------------------------------------------------------------------------------
		/// 
		Task<bool> Clear();
		/// 
		/// ------------------------------------------------------------------------------------------------
		/// Name		LoadCaseData
		/// 
		/// <summary>	Loads the case data from the device.
		/// </summary>
		/// <remarks>
		/// </remarks>
		/// ------------------------------------------------------------------------------------------------
		/// 
		Task<bool> LoadCaseData();
		/// 
		/// ------------------------------------------------------------------------------------------------
		/// Name		LoadUser
		/// 
		/// <summary>	Loads the user data from the device.
		/// </summary>
		/// <remarks>
		/// </remarks>
		/// ------------------------------------------------------------------------------------------------
		/// 
		void LoadUser();
		/// 
		/// ------------------------------------------------------------------------------------------------
		/// Name		SaveCaseData
		/// 
		/// <summary>	Saves the case data to the device.
		/// </summary>
		/// <remarks>
		/// </remarks>
		/// ------------------------------------------------------------------------------------------------
		/// 
		Task<bool> SaveCaseData();
		/// 
		/// ------------------------------------------------------------------------------------------------
		/// Name		SaveConfig
		/// 
		/// <summary>	Saves the config data to the device.
		/// </summary>
		/// <remarks>
		/// </remarks>
		/// ------------------------------------------------------------------------------------------------
		/// 
		Task<bool> SaveConfig();
		/// 
		/// ------------------------------------------------------------------------------------------------
		/// Name		saveUser
		/// 
		/// <summary>	Saves the user data to the device.
		/// </summary>
		/// <remarks>
		/// </remarks>
		/// ------------------------------------------------------------------------------------------------
		/// 
		Task<bool> SaveUser();
		/// 
		#endregion
		/// ------------------------------------------------------------------------------------------------
	}
}
